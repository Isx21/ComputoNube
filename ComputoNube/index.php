    <h1>ESTUDIO</h1>
    <p align="justify">La computación en la nube es la entrega de recursos informáticos a través de Internet. Ofrece ahorro de costes, escalabilidad, alto rendimiento, economías de escalado y mucho más. Para muchas empresas, la migración a la nube está directamente relacionada con la modernización de datos y TI.
        Cuando se empezó a hablar de “la nube” a principios de la década de los 2000, la expresión tenía una especie de halo misterioso. La idea de acceder a los recursos informáticos desde otro lugar que no sea una infraestructura de TI local (¿el cielo, acaso?) sonaba como a ciencia ficción. La realidad era mucho más profunda y cambió para siempre la tecnología y la forma de hacer negocios.
        <br>
        <br>
        ¿Qué es la computación en la nube?
        <br>
        La computación en la nube es la entrega de recursos informáticos, incluidos el almacenamiento, la potencia de procesamiento, las bases de datos, las redes, los análisis, la inteligencia artificial y las aplicaciones de software, a través de Internet (la nube). Al externalizar estos recursos, las empresas pueden acceder a los activos informáticos que necesitan, en el momento en que los necesitan y sin tener que comprar ni mantener una infraestructura física local de TI. Esto proporciona recursos flexibles, innovación más rápida y economías de escalado. Para muchas empresas, la migración a la nube está directamente relacionada con la modernización de datos y TI.[1]</p>
    <p align="justify">¿Qué es computación en la nube?<br>

        La computación en la nube (o cloud computing, en inglés) es una tecnología que permite acceder remotamente, de cualquier lugar del mundo y en cualquier momento, a softwares, almacenamiento de archivos y procesamiento de datos a través de Internet, sin la necesidad de conectarse a un ordenador personal o servidor local.
        En otras palabras, la computación en la nube utiliza la conectividad y gran escala de Internet para hospedar los más variados recursos, programas e información, y permite que el usuario ingrese a ellos través de cualquier ordenador, tablet o celular.
        Si todavía no quedó claro, déjame ejemplificarlo: si alguna vez editaste un documento en Google Docs, escuchaste música en Spotify o viste una película en Netflix, tuviste contacto directo con la computación en la nube. Todos ellos son servicios online que no demandan la descarga, instalación y acceso de ningún tipo de software o archivo en tu ordenador. Solo necesitas un navegador y una buena conexión a Internet para utilizarlos.[2]</p>
    <p align="justify">¿Cuáles son las ventajas de la computación en nube?<br>

        La computación en nube ofrece a las empresas modelos prácticos para acceder a las ofertas de infraestructura, plataforma y software de pago por uso. Con la computación en nube, las empresas pueden liberar capital, optimizar el mantenimiento de TI, modernizar y escalar los enfoques empresariales, convertir la seguridad y la flexibilidad en servicios y soluciones, ayudar a los clientes de nuevas maneras, y hacer crecer su empresa en las condiciones de mercado siempre cambiantes.[3]</p>
